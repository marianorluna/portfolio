function isExternal(href: string): boolean {
  return href.startsWith("http://") || href.startsWith("https://");
}

export function ContactSocialRow({ contactSocial }: Props) {
  const { items, navAriaLabel } = contactSocial;
  if (items.length === 0) return null;

  return (
    <nav className="nav-contact-social" aria-label={navAriaLabel}>
      <ul className="nav-contact-social__list">
        {items.map((link: ContactSocialLink) => (
          <li key={link.id} className="nav-contact-social__item">
            <a
              href={link.href}
              className="nav-contact-social__btn"
              aria-label={link.label}
              {...(isExternal(link.href) ? { target: "_blank", rel: "noreferrer" } : {})}
            >
              <span className="nav-contact-social__icon" aria-hidden>
                <SocialGlyph id={link.id} />
              </span>
            </a>
          </li>
        ))}
      </ul>
    </nav>
  );
}
